package org.apache.fineract.gatway.weaver.data;

import java.io.Serializable;

public final class LoginData implements Serializable {

    private final Long id;
    private final String email;
    private final String password;

    public static LoginData instance(final Long id, final String email, final String password) {
        return new LoginData(id, email, password);
    }

    public LoginData(Long id, String email, String password) {
        this.id = id;
        this.email = email;
        this.password = password;
    }

    @Override
    public String toString() {
        return "LoginData{" + "id=" + id + ", email='" + email + '\'' + ", password='" + password + '\'' + '}';
    }

    public Long getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }
}
